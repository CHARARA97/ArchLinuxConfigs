#!/usr/bin/env python3
import wcwidth
lines = [
    '┌────┬────┬────┬────┬────┬────┬────┬────┬────┬────┬────┬────┬────┬──────────┐',
    '│ $~ │ &% │ [7 │ {5 │ }3 │ (1 │ =9 │ *0 │ )2 │ +4 │ ]6 │ !8 │ #` │   Tab    │',
    '├────┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬─────────┤',
    '│ <-- │ ;: │ ,< │ .> │  P │  Y │  F │  G │  C │  R │  L │ /? │ @^ │   \\|    │',
    '├─────┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴─────────┤',
    '│ Ctrl │  A │  O │  E │  U │  I │  D │  H │  T │  N │  S │ -_ │  Enter      │',
    '├──────┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴┬───┴─────────────┤',
    '│ Shift │ \'" │  Q │  J │  K │  X │  B │  M │  W │  V │  Z │   Shift         │',
    '└───────┴────┴────┴────┴────┴────┴────┴────┴────┴────┴────┴───────────────────┘',
]
for i, l in enumerate(lines):
    col = 0
    for c in l:
        col += max(1, wcwidth.wcwidth(c))
    print(f"Line {i}: {len(l.encode('utf-8'))} bytes, {col} cols")
